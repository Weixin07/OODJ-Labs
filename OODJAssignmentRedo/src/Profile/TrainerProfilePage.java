    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        if (txtName.getText().equals("") || txtEmail.getText().equals("") || txtContactNumber.getText().equals("") || txtPassword.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter data into the textbox.");
        } else {
            String data[] = {txtEmail.getText(), txtPassword.getText(), txtName.getText(), txtContactNumber.getText()};

            DefaultTableModel Table = (DefaultTableModel) tableView.getModel();
            Table.addRow(data);
            JOptionPane.showMessageDialog(this, "User Added!");

            txtEmail.setText("");
            txtPassword.setText("");
            txtName.setText("");
            txtContactNumber.setText("");

            ExportToTrainerDetails();
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void tableViewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableViewMouseClicked
        DefaultTableModel Table = (DefaultTableModel) tableView.getModel();

        String Email = Table.getValueAt(tableView.getSelectedRow(), 0).toString();
        String Password = Table.getValueAt(tableView.getSelectedRow(), 1).toString();
        String Name = Table.getValueAt(tableView.getSelectedRow(), 2).toString();
        String ContactNumber = Table.getValueAt(tableView.getSelectedRow(), 3).toString();

        txtEmail.setText(Email);
        txtPassword.setText(Password);
        txtName.setText(Name);
        txtContactNumber.setText(ContactNumber);
    }//GEN-LAST:event_tableViewMouseClicked

