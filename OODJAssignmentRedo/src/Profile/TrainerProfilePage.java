    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (txtName.getText().equals("") || txtEmail.getText().equals("") || txtContactNumber.getText().equals("") || txtPassword.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter data into the textbox.");
        } else {
            DefaultTableModel Table = (DefaultTableModel) tableView.getModel();
            int SelectedRow = tableView.getSelectedRow();

            if (SelectedRow >= 0) {

                String EmailUpdate = txtEmail.getText();
                String Password = txtPassword.getText();
                String Name = txtName.getText();
                String ContactNumber = txtContactNumber.getText();

                Table.setValueAt(EmailUpdate, SelectedRow, 0);
                Table.setValueAt(Password, SelectedRow, 1);
                Table.setValueAt(Name, SelectedRow, 2);
                Table.setValueAt(ContactNumber, SelectedRow, 3);

                JOptionPane.showMessageDialog(null, "The user details is updated.", "APU Gym Centre", JOptionPane.PLAIN_MESSAGE);

                txtEmail.setText("");
                txtPassword.setText("");
                txtName.setText("");
                txtContactNumber.setText("");

            }
            ExportToTrainerDetails();
        }


    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        DefaultTableModel Table = (DefaultTableModel) tableView.getModel();

        if (tableView.getSelectedRowCount() == 1) {
            Table.removeRow(tableView.getSelectedRow());

            txtEmail.setText("");
            txtPassword.setText("");
            txtName.setText("");
            txtContactNumber.setText("");

            ExportToTrainerDetails();

        } else {
            if (tableView.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "The database is empty. No user is deleted.");
            } else {
                JOptionPane.showMessageDialog(this, "Please select only one user at a time.");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

