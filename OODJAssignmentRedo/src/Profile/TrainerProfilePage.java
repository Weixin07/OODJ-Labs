    void ViewTrainerDetails() {
        String filePath = "D:\\APU Stuff\\Bachelor of Computer Science (Intelligent Systems) [APD2F2202CS(IS)]\\Semester 1\\Object Oriented Development with Java [CT038-3-2-OODJ]\\Group Assignment\\System\\OODJAssignmentRedo\\UserDetails\\TrainerDetails.txt";
        File file = new File(filePath);

        try {
            FileReader fr = new FileReader(file);
            BufferedReader br = new BufferedReader(fr);

            DefaultTableModel model = (DefaultTableModel) tableView.getModel();
            Object[] lines = br.lines().toArray();

            for (int i = 0; i < lines.length; i++) {
                String[] row = lines[i].toString().split(" ");
                model.addRow(row);
            }

            br.close();

        } catch (FileNotFoundException ex) {
            java.util.logging.Logger.getLogger(TrainerProfilePage.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(TrainerProfilePage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    void ExportToTrainerDetails() {
        String filePath = "D:\\APU Stuff\\Bachelor of Computer Science (Intelligent Systems) [APD2F2202CS(IS)]\\Semester 1\\Object Oriented Development with Java [CT038-3-2-OODJ]\\Group Assignment\\System\\OODJAssignmentRedo\\UserDetails\\TrainerDetails.txt";
        File file = new File(filePath);

        try {
            FileWriter fw = new FileWriter(file);
            BufferedWriter bw = new BufferedWriter(fw);

            for (int i = 0; i < tableView.getRowCount(); i++) {
                for (int j = 0; j < tableView.getColumnCount(); j++) {
                    bw.write(tableView.getValueAt(i, j).toString() + " ");
                }
                bw.newLine();
            }

            bw.close();
            fw.close();

        } catch (IOException ex) {
            Logger.getLogger(TrainerProfilePage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

